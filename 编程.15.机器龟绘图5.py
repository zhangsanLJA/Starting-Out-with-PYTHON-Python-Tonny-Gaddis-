import turtle
turtle.circle(5)
turtle.penup()
turtle.goto(55,0)
turtle.write('East')
turtle.goto(50,0)
turtle.pendown()
turtle.goto(-50,0)
turtle.penup()
turtle.goto(-55,0)
turtle.write('West')
turtle.penup()
turtle.goto(0,55)
turtle.write('North')
turtle.goto(0,50)
turtle.pendown()
turtle.goto(0,-50)
turtle.penup()
turtle.goto(0,-55)
turtle.write('South')
