def main():
    print(f'Joe购买的股票付款总额： {40.00*2000:^8.2f}')
    print(f'Joe购买股票的代理人支付的金额：{40.00*2000*0.03:^8.2f}')
    print(f'Joe卖出股票后的总收益：{42.75*2000:^8.2f}')
    print(f'Joe给代理人的总收益：{42.75*2000*0.03:^8.2f}')
    if (42.75*2000*(1-0.03)-40*2000*(1-0.03))>0:
        print('Joe赚钱了')
    else:
        print('Joe没赚钱')

if __name__=='__main__':
    main()
